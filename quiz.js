// ======= Fonction de correction =======
function corrigerQuiz() {
    score = 0;

    questions.forEach((q, questionIndex) => {
        const options = document.querySelectorAll(`input[name="q${questionIndex}"]`);
        const bonneReponse = q.reponse;

        options.forEach((option, optionIndex) => {
            const label = option.parentElement;

            // Bonne réponse → vert
            if (optionIndex === bonneReponse) {
                label.style.color = "green";
                label.style.fontWeight = "bold";
            }

            // Mauvaise réponse cochée → rouge
            if (option.checked && optionIndex !== bonneReponse) {
                label.style.color = "red";
                label.style.fontWeight = "bold";
            }

            // Empêcher de modifier après soumission
            option.disabled = true;
        });

        // Calcul du score
        const selected = document.querySelector(`input[name="q${questionIndex}"]:checked`);
        if (selected && parseInt(selected.value) === bonneReponse) {
            score++;
        }

        // Ajouter l'explication
        const explanation = document.createElement("p");
        explanation.textContent = "💡 " + q.explication;
        explanation.style.fontStyle = "italic";
        explanation.style.marginTop = "5px";
        document.getElementById(`question-${questionIndex}`).appendChild(explanation);
    });

    // Affichage des statistiques + bouton rejouer
    const statsContainer = document.getElementById("stats");
    statsContainer.innerHTML = `
        <h3>Résultats</h3>
        <p>Tu as eu ${score}/${questions.length} bonnes réponses</p>
        <p>Donnez votre avis sur le travail et invitez vos amis à jouer.</p>
        <p>+2290151964328</p>
    `;

    const replayBtn = document.getElementById("replay-btn");
    replayBtn.style.display = "inline-block";
    replayBtn.style.margin = "10px";
    replayBtn.addEventListener("click", () => {
        window.location.href = "categories.html";
    });

    submitBtn.style.display = "none"

    // Empêcher double soumission
    submitBtn.disabled = true;
}