// ======= Affichage des questions =======
function afficherQuestions() {
    const form = document.getElementById("quiz-form");
    form.innerHTML = "";

    questions.forEach((q, i) => {
        const div_question = document.createElement("div");
        div_question.classList.add("question");
        div_question.id = `question-${i}`;

        const h3 = document.createElement("h3");
        h3.textContent = `Question ${i + 1}: ${q.question}`;
        div_question.appendChild(h3);

        q.possibilites.forEach((opt, index) => {
            const label = document.createElement("label");
            const input = document.createElement("input");

            input.type = "radio";
            input.name = `q${i}`;
            input.value = index;

            label.appendChild(input);
            label.appendChild(document.createTextNode(opt));
            div_question.appendChild(label);
            div_question.appendChild(document.createElement("br"));
        });

        form.appendChild(div_question);
    });
}
